package Exceptions;

public class NotResearchTreeException extends Exception {

	private static final long serialVersionUID = 1L;

	public NotResearchTreeException() {
		super();
	}

	public NotResearchTreeException(String arg0) {
		super(arg0);
		
	}

	public NotResearchTreeException(Throwable arg0) {
		super(arg0);
	}

	public NotResearchTreeException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
